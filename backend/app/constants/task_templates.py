def checklist_markdown(kind: str | None, status: str) -> str | None:
    if not kind:
        kind = ProjectKind.general.value
    tmpl = DEFAULT_CHECKLISTS.get(kind, {}).get(status)
    if not tmpl:
        return None
    lines = ["Чеклист:", *[f"- [ ] {x}" for x in tmpl]]
    return "\n".join(lines).strip() + "\n"


def checklist_items(kind: str | None, status: str) -> tuple[str, ...]:
    if not kind:
        kind = ProjectKind.general.value
    return DEFAULT_CHECKLISTS.get(kind, {}).get(status, ())


def column_hint(kind: str | None, status: str) -> str | None:
    if not kind:
        kind = ProjectKind.general.value
    return COLUMN_HINTS.get(kind, {}).get(status)

